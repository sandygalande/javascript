console.log("<=========== Assignment 2 (using for loop) ===========>");
console.log(`<===== Step 1: WAP to print numbers from 5 to 15 =====>`);
for (let i = 5; i <= 15; i++) {
  console.log(i);
}

console.log(`<===== Step 2: WAP to print numbers from 50 to 40 =====>`);
for (let i = 50; i >= 40; i--) {
  console.log(i);
}

console.log(`<===== Step 3: WAP to find first 15 odd numbers =====>`);
for (let i = 1; i <= 30; i = i + 2) {
  console.log(i);
}

console.log(`<===== Step 4: WAP to find first 10 even numbers =====>`);
for (let i = 2; i <= 20; i = i + 2) {
  console.log(i);
}

console.log(`<===== Step 5: WAP to print table of 5 =====>`);
for (let i = 5; i <= 50; i = i + 5) {
  console.log(i);
}

console.log(`<===== Step 6: WAP to print table of 10 =====>`);
for (let i = 10; i <= 100; i = i + 10) {
  console.log(i);
}

console.log(`<===== Step 7: WAP to print table of 10 in reverse order =====>`);
for (let i = 100; i >= 10; i = i - 10) {
  console.log(i);
}
